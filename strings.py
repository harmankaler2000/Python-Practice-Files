name = "harmandeep kaur Kaler"
#length function
print(len(name))
#returns the first index where the input occurs
print(name.find("a"))
#capitalizes the first letter of the string only
print(name.capitalize())
#converts string to upper case
print(name.upper())
#converts string to lower case
print(name.lower())
#true or false if the string is digit or not
print(name.isdigit())
#checks if string is alphabetical
#will return false if space is there
print(name.isalpha())
#count char in string
print(name.count('a'))
#replace value with another in a string
print(name.replace('a', 'o'))
#display string multiple times
print(name*3)